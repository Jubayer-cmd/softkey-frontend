'use client';
import axios from 'axios';
import { useUserLoginMutation } from '@/redux/api/authApi';
import { storeUserInfo } from '@/services/auth.service';
import { yupResolver } from '@hookform/resolvers/yup';
import { message } from 'antd';
import Image from 'next/image';
import { useRouter } from 'next/navigation';
import { Controller, SubmitHandler, useForm } from 'react-hook-form';
import * as Yup from 'yup';
import Link from 'next/link';
import NavbarPage from '../home/navbar';
const schema = Yup.object().shape({
  email: Yup.string()
    .required('Email is required')
    .email('Invalid email format'),
  password: Yup.string().required('Password is required'),
});
type FormValues = {
  email: string;
  password: string;
};
function Login() {
  const [userLogin] = useUserLoginMutation();
  const router = useRouter();
  const {
    handleSubmit,
    control,
    formState: { errors },
  } = useForm({
    resolver: yupResolver(schema),
  });

  const onSubmit: SubmitHandler<FormValues> = async (data: any) => {
    try {
      const result = await axios.post(
        'https://softkey-backend.vercel.app/api/v1/auth/login',
        { ...data }
      );
      console.log('result', result);

      if (result?.data?.token) {
        router.push('/');
        message.success('User logged in successfully!');
        storeUserInfo({ accessToken: result?.data?.token });
      }
    } catch (err: any) {
      console.error(err);
      if (err.response && err.response.status === 401) {
        // Handle specific unauthorized errors
        message.error('Invalid credentials. Please try again.');
      } else {
        // Handle other errors
        message.error('An error occurred. Please try again later.');
      }
    }
  };

  return (
    <div>
      <NavbarPage />
      <div className="min-h-screen flex items-center justify-center bg-gray-50 py-12 px-4 sm:px-6 lg:px-8">
        <div className="w-1/2">
          <Image
            src={
              'https://img.freepik.com/free-vector/computer-login-concept-illustration_114360-7962.jpg'
            }
            width={500}
            height={500}
            alt="login image"
          />
        </div>
        <div className="w-1/2 bg-white p-8 shadow-md">
          <form onSubmit={handleSubmit(onSubmit)} className="space-y-6">
            <div>
              <label
                htmlFor="email"
                className="block text-sm font-medium text-gray-700"
              >
                Email
              </label>
              <Controller
                name="email"
                control={control}
                render={({ field }) => (
                  <input
                    {...field}
                    type="text"
                    id="email"
                    className={`mt-1 focus:ring-indigo-500 focus:border-indigo-500 block w-full py-2 px-3 border border-gray-300 rounded-md ${
                      errors.email ? 'border-red-500' : ''
                    }`}
                  />
                )}
              />
              {errors.email && (
                <p className="mt-2 text-sm text-red-600">
                  {errors.email.message}
                </p>
              )}
            </div>

            <div>
              <label
                htmlFor="password"
                className="block text-sm font-medium text-gray-700"
              >
                Password
              </label>
              <Controller
                name="password"
                control={control}
                render={({ field }) => (
                  <input
                    {...field}
                    type="password"
                    id="password"
                    className={`mt-1 focus:ring-indigo-500 focus:border-indigo-500 block w-full py-2 px-3 border border-gray-300 rounded-md ${
                      errors.password ? 'border-red-500' : ''
                    }`}
                  />
                )}
              />
              {errors.password && (
                <p className="mt-2 text-sm text-red-600">
                  {errors.password.message}
                </p>
              )}
            </div>

            <div className="mt-4">
              <button
                type="submit"
                className="group relative w-full flex justify-center py-2 px-4 border border-transparent text-sm font-medium rounded-md text-white bg-indigo-600 hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500 transform transition-transform hover:scale-105 ease-in-out duration-300"
              >
                Log in
              </button>
              <p>
                New to softkey?
                <Link href="/registration" className="text-blue-500">
                  registration
                </Link>
              </p>
            </div>
          </form>
        </div>
      </div>
    </div>
  );
}

export default Login;
